    private String capturarRadio(){
        JRadioButton radio;
        String str = null;
        Enumeration jr = grTipo.getElements();
        while ( jr.hasMoreElements() )
        {
            radio = (JRadioButton) jr.nextElement();
            if (radio.isSelected())
            {
                str = radio.getText();
            }
        }
        return str;
    }
    
    private void marcarRadio(String str){
        JRadioButton radio;
        Enumeration jr = grTipo.getElements();
        while ( jr.hasMoreElements() ) {
            radio = (JRadioButton) jr.nextElement();
            if (radio.getText().equals(str))
                radio.setSelected(true);
        }
    }
    
    private void preencherCampos(String codigoBarra, String nome, String laboratorio, 
    String valorUnitario, String quantidade, String localizacao, String composicao, String tipo){
        tfCodigoBarra.setText(codigoBarra);
        tfNome.setText(nome);
        tfLaboratorio.setText(laboratorio);
        tfValorUnitario.setText(valorUnitario);
        tfQuantidade.setText(quantidade);
        cbLocalizacao.setSelectedItem(localizacao);
        taComposicao.setText(composicao);
        marcarRadio(tipo);
    }
  
    private Medicamento buscarPorNome(String nome){
        Medicamento med = null;
        for(Medicamento m : medicamentos){
            if(m.getNome().equals(nome)){
                med = m;
            }
        }
        return med;
    }
    
    private Medicamento buscarPorCodigo(String codigo){
        Medicamento med = null;
        for(Medicamento m : medicamentos){
            if(m.getCodigoBarra().equals(codigo)){
                med = m;
            }
        }
        return med;
    }
    
