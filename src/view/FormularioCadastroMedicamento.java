    private void btInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btInserirActionPerformed
        try{
            String codigoBarra = tfCodigoBarra.getText();
            String nome = tfNome.getText();
            String laboratorio = tfLaboratorio.getText();
            double valorUnitario = Double.parseDouble(tfValorUnitario.getText());
            int quantidade = Integer.parseInt(tfQuantidade.getText());
            String localizacao = cbLocalizacao.getSelectedItem().toString();
            String composicao = taComposicao.getText();
            String tipo = capturarRadio();
            medicamento = new Medicamento(codigoBarra, nome, laboratorio, valorUnitario, composicao,
            tipo, quantidade, localizacao);
            medicamentos.add(medicamento);
            JOptionPane.showMessageDialog(null, "Medicamento cadastrado com sucesso!", "Finalizado!", 
            JOptionPane.INFORMATION_MESSAGE);
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos!", "Atenção!", 
            JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btInserirActionPerformed

    private void btBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btBuscarActionPerformed
        String nome = tfNome.getText();
        String codigoBarra = tfCodigoBarra.getText();
        medicamento = buscarPorNome(nome);
        if(medicamento != null){
            preencherCampos(medicamento.getCodigoBarra(), medicamento.getNome(), 
            medicamento.getLaboratorio(), Double.toString(medicamento.getValorUnitario()),
            Integer.toString(medicamento.getQuantidadeEstoque()), 
            medicamento.getLocalizacao(),medicamento.getComposicao(), 
            medicamento.getTipo());
        }else{
            medicamento = buscarPorCodigo(codigoBarra);
            if(medicamento != null){
                 preencherCampos(medicamento.getCodigoBarra(), medicamento.getNome(), 
                 medicamento.getLaboratorio(), Double.toString(medicamento.getValorUnitario()),
                 Integer.toString(medicamento.getQuantidadeEstoque()), 
                 medicamento.getLocalizacao(),medicamento.getComposicao(), 
                 medicamento.getTipo());
            }else{
                JOptionPane.showMessageDialog(null, "Medicamento não encontrado!", "Atenção!", 
                JOptionPane.INFORMATION_MESSAGE);
            }
        }
    }//GEN-LAST:event_btBuscarActionPerformed

    private void btVenderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btVenderActionPerformed
        try{
            int quantidade = Integer.parseInt(JOptionPane.showInputDialog(null, "Informe a quantidade a ser "
            + "comprada!", "Atenção!", JOptionPane.QUESTION_MESSAGE));
            medicamento.locar(quantidade);
            double valorTotalVenda = medicamento.calcularVatorTotalVenda(quantidade);
            JOptionPane.showMessageDialog(null, "Venda finalizada com valor de R$ " + valorTotalVenda);
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Informe apenas número!", "Atenção!",
            JOptionPane.WARNING_MESSAGE);
        }catch (Excecao ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }catch(NullPointerException ex){
           JOptionPane.showMessageDialog(null, "Busque um medicamento primeiro!", "Atenção!", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btVenderActionPerformed

